#include <iostream>
using namespace std;

int main(){
    float angka1, angka2;
    cout << "Masukan angka 1 : ";
    cin >> angka1;

    cout << "Masukan angka 2 : ";
    cin >> angka2;

    cout << "Penjumlahan : " << angka1 + angka2 <<endl;
    cout << "Pengurangan : " << angka1 - angka2 << endl;
    cout << "Perkalian : " << angka1 * angka2 << endl;
    cout << "Pembagian : " << angka1 / angka2 << endl;

    return 0;

}